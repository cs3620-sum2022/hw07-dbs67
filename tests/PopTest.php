declare(strict_types=1);

namespace App\Tests;

use App\Stack;
use PHPUnit\Framework\TestCase;

/**
 * Pop Test
 *
 * @category UnitTests
 * @package  App\Tests
 * @author   Don Stringham <donstringham@weber.edu>
 * @license  MIT https://opensource.org/licenses/MIT
 * @link     https://weber.edu
 */
class PopTest extends TestCase
{
    protected $harness;

    /**
     * Test
     *
     * @category UnitTests
     * @package  App\Tests
     * @author   Don Stringham <donstringham@weber.edu>
     * @license  MIT https://opensource.org/licenses/MIT
     * @link     https://weber.edu
     * @return   null
     */
    public function setUp(): void
    {
        $this->harness = new Stack();
    }

    /**
     * Test
     *
     * @category UnitTests
     * @package  App\Tests
     * @author   Don Stringham <donstringham@weber.edu>
     * @license  MIT https://opensource.org/licenses/MIT
     * @link     https://weber.edu
     * @return   null
     */
    public function testPopSuccess()
    {
        // arrange
        $this->harness->push(101);
        // act
        $actual = $this->harness->pop();
        // assert
        $this->assertEquals(101, $actual);
    }

    /**
     * Test
     *
     * @category UnitTests
     * @package  App\Tests
     * @author   Don Stringham <donstringham@weber.edu>
     * @license  MIT https://opensource.org/licenses/MIT
     * @link     https://weber.edu
     * @return   null
     */
    public function testPopFail()
    {
        // arrange & act
        $actual = $this->harness->pop();
        // assert
        $this->assertNotEquals(null, $actual);
    }
}
